def generate_short_names(full_name: str) -> list:
    """Generate common abbreviations from full school name."""
    names = [full_name]

    # Add abbreviations from map
    if full_name in ABBREV_MAP:
        names.extend(ABBREV_MAP[full_name])

    # Strip "學校財團法人" prefix patterns for matching
    # e.g. "輔仁大學學校財團法人輔仁大學" → "輔仁大學"
    cleaned = re.sub(r"學校財團法人", "", full_name)
    if cleaned != full_name:
        names.append(cleaned)

    # 臺/台 variants
    extra = []
    for n in names:
        if "臺" in n:
            extra.append(n.replace("臺", "台"))
        elif "台" in n:
            extra.append(n.replace("台", "臺"))
    names.extend(extra)

    return list(dict.fromkeys(names))  # dedupe preserving order


def make_config(school_name, address, president):
    """Create a config dict for one school."""
    school_terms = generate_short_names(school_name)

    people_terms = []
    if president and president != "未知":
        people_terms.append(president)

    address_terms = []
    if address:
        address_terms.append(address)

    custom_terms = {"schools": school_terms}
    if people_terms:
        custom_terms["people"] = people_terms
    if address_terms:
        custom_terms["addresses"] = address_terms

    return {
        "version": 1,
        "custom_terms": custom_terms,
        "file_types": [
            ".txt", ".md", ".docx", ".xlsx", ".pptx", ".pdf",
            ".jpg", ".jpeg", ".png", ".bmp", ".tiff",
        ],
        "logo_templates": [],
        "substring_match": True,
    }


def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    for code, name, address, president in SCHOOLS:
        config = make_config(name, address, president)
        safe_name = name.replace(" ", "_")
        zip_name = f"{code}_{safe_name}.anonymizer-config.zip"
        zip_path = os.path.join(OUTPUT_DIR, zip_name)

        with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
            config_json = json.dumps(config, ensure_ascii=False, indent=2)
            zf.writestr("config.json", config_json)

        print(f"  {zip_name}")

    print(f"\n共產生 {len(SCHOOLS)} 組設定檔，位於 {OUTPUT_DIR}/")


if __name__ == "__main__":
    main()
